package ch06;

public class ConEx02 {

    public static void main(String[] args) {

        int point = 80;

        // 아버지 :   성적이 90점이 넘으면 차를 사주마!!
        //          하지만 90점이 안되면 벌을 주마!!
        if(point > 90){
            System.out.println("옛다 차!!");
        }else{
            System.out.println("벌 받아라..");
        }
    }
}
