package ch03;

// 사칙연산 +, -, /, *
public class OperEx02 {

    public static void main(String[] args) {
        int n1 = 3;
        int n2 = 4;

        int sum = n1 + n2;              // 수치 연산
        System.out.println("sum = " + sum);

        String s1 = "반가워!! 나의 나이는 ";
        int age = 25;
        System.out.println(s1 + age);   // 결합

        System.out.println(5 - 3);

        System.out.println(10 / 2);

        System.out.println(2 * 20);
    }
}
